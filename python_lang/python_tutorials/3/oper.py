# Division
print(17/3) # classic division returns float
print(17//3) # floor division discards the fractional part
print(17%3) # the % operator return the remainder of the division
print(5*3 + 2) # floored quotient * divior + remainder
print((17//3)*3 + (17%3)) # basically the above equation

# To calculate powers we just use ** in python. x ** y means x^y
print(2**3) # 2^3 = 8
print(3**4) # 3^4 = 81

# operators with mix type operands convert the integer operant to float by itself. 
print(4 * 3.75 -1) #14

