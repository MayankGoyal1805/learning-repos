package pw.lecture5;

public class ArithmeticOperations {
    public static void main(String[] args) {
        int p =10 , q =20 , r =30;
        System.out.println(p+q);
        System.out.println(p-q);
        System.out.println(p*q);
        System.out.println(p/q);
        System.out.println(p%q);
        System.out.println(r%q);
        System.out.println((float)p /q);
    }
    
}
