package pw.lecture3;

public class DataTypes {
    public static void main(String[] args) {
        boolean flag = true;
        int a = 12345;
        short s = 1200;
        byte b = 120;
        long l = 843038302;
        float f = 3934.47834f;
        double d = 34434.2494;
        char ph = 'p';
        System.out.println("The boolean variable: " +flag);
        System.out.println("The integer variable: " +a);
        System.out.println("The short variable: " +s);
        System.out.println("The byte variable: " +b);
        System.out.println("The long variable: " +l);
        System.out.println("The float variable: " +f);
        System.out.println("The double variable: " +d);
        System.out.println("The char variable: " +ph);
    }
}
